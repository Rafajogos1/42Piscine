char	*ft_strupcase(char *str)
{
	int		i;
	char	letter;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			letter = str[i];
			str[i] = (letter - 32);
		}
		i++;
	}
	return (str);
}
