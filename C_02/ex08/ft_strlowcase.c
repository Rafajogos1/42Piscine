char	*ft_strlowcase(char *str)
{
	int		i;
	char	letter;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			letter = str[i];
			str[i] = (letter + 32);
		}
		i++;
	}
	return (str);
}
